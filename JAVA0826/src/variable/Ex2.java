package variable;

public class Ex2 {

	public static void main(String[] args) {
		/*
		 * Variable: 
		 * - 데이터를 저장하는 메모리 공간
		 * - 변수 공간에는 한 번에 하나의 데이터만 저장 됨 
		 * - 변수를 사용하기 위해서는 반드시 변수의 데이터 선언 
		 * - 데이터를 변수에 저장하는 작업을 변수의 초기화라고 함 
		 * - 변수명 작성할 때는 '식별자 작성 규칙'을 따름
		 * 
		 * <식별자 작성 규칙>
		 * 
		 * 
		 * OS에 메모리를 요청 
		 * 
		 * <변수 선언 기본 문법>
		 * Data Type syntax
		 * 
		 * <변수 초기화 기본 문법>
		 * 변수명 = 데이터;
		 * 
		 */
		
		// Declear integer variable
		int a;
		
		// Initialise variable 
		a = 20;
		
		System.out.println(a);
//		The local variable a may not have been initialized
	}

}
